create policy "trainers can read class_attendance"
on public.class_attendance
for select
to authenticated
using (
  exists (
    select 1
    from public.class_sessions class_session
    join public.class_trainers class_trainer
      on class_trainer.class_id = class_session.class_id
    join public.profiles profile
      on profile.id = auth.uid()
    where class_session.id = session_id
      and class_trainer.profile_id = auth.uid()
      and 'Trainer' = any(coalesce(profile.titles, '{}'::text[]))
  )
  and not public.is_admin()
);
